    <h2 class="sousTitres">Notre collectif</h2>
    <div class="line"></div>
</div>
<div>
    <img src="assets/img/photoEquipe/notre_collectif.jpg" alt="notre collectif" class="pleinEcran">
</div>
<section class="colonnesTexte">
    <article class="articleInfos">
        <p>Formé en 2015, notre collectif réunit deux designers et une architecte autour de la réflexion des usages de la ville.</p>
        <p>Nos compétences, venues d’ailleurs, nous permettent d’agir sur différentes échelles, du mobilier à la micro-architecture en passant par l’élément mobile pour traverser la ville ou encore des installations urbaines transitoires.</p>
    </article>
    <article class="articleInfos">
        <p>Nous définissons l’espace urbain comme le lieu idéal où pratiquer notre design ancré dans la réalité. Une mise en oeuvre que l’on veut temporaire, jamais pérenne, la ville évoluant trop vite pour que l’on puisse la figer.</p>
    </article>
    <article class="articleInfos">
        <p>L’espace public est le théâtre de tous les gestes, toutes les histoires, toutes les rencontres. Il offre la meilleure réflexion à notre travail, à travers les rapports avec ses usagers. C’est pour, avec, et à travers eux que l’on travaille.</p>
        <p>Nous aimons dialoguer, passer un temps avec eux, échanger nos expériences et nos idées, pour ainsi enrichir nos projets.</p>
    </article>
</section>
